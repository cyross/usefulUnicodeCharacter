const generateRange = (start: number, end: number): number[] => {
  // startからendまでの範囲を生成
  const array: number[] = [];
  
  for (let i = start; i <= end; i++) {
    array.push(i);
  }

  return array;
}

const generateLetters = (start: number, end: number) =>
    generateRange(start, end).map((code) => String.fromCharCode(code));

